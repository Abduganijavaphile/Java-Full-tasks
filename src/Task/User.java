package Task;

public class User {
    String firstName;
    String lastName;
    String email;
    String phoneNumber;
    String password;

    User(String firstName, String lastName, String email, String phoneNumber, String password){
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.password = password;
    }



}
